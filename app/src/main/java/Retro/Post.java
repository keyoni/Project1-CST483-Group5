package Retro;

public class Post {

    private int userId;
    private int id;
    private String type;
    private String gender;
    private String age;
    private String name;

    public int getUserId() {
        return userId;
    }

    public int getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public String getGender() {
        return gender;
    }

    public String getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
}
